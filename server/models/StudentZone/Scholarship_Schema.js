const mongoose = require('mongoose');

const Scholarship_Schema = mongoose.Schema({
    title: {
        type: String,
        required: true,
        trim: true
    },
    link: {
        type: String,
        required: true,
        trim: true
    },
    file_path: {
        type: String,
        required: true
    },
    file_mimetype: {
        type: String,
        required: true
    }
}, {
    timestamps: true
});

const Scholarship = mongoose.model('Scholarship', Scholarship_Schema);

module.exports = Scholarship;