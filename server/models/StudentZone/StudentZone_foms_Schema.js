const mongoose = require('mongoose');

const StudentZone_foms_Schema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true
    },
    link: {
      type: String,
      required: true,
      trim: true
    },
    file_path: {
      type: String,
      required: true
    },
    file_mimetype: {
      type: String,
      required: true
    }
  },
  {
    timestamps: true
  }
);

const Student_forms = mongoose.model('Student_forms', StudentZone_foms_Schema);

module.exports = Student_forms;