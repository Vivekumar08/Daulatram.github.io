const mongoose = require('mongoose');

const Stud_Hostel_Schema = mongoose.Schema(
    {
        title: {
            type: String,
            required: true,
        },
        description: {
            type: String,
            required: true,
        },
        img_data: {

            file_path: [
                {
                    file_path1: {
                        type: String,
                        default: null
                    },
                    file_mimetype1: {
                        type: String,
                        default: null
                    },
                }
            ],
            pdf_path: [
                {
                    title: {
                        type: String,
                        default: null
                    },
                    pdf_path1: {
                        type: String,
                        default: null
                    },
                    pdf_mimetype1: {
                        type: String,
                        default: null
                    },
                    value: {
                        type: String,
                        default: null
                    },
                }
            ],
            para: [
                {
                    para1: {
                        type: String,
                    },
                }
            ],
        }
    },
    {
        timestamps: true
    }
);

const Stud_Hostel = mongoose.model('Stud_Hostel', Stud_Hostel_Schema);

module.exports = Stud_Hostel;