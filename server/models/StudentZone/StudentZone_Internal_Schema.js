const mongoose = require('mongoose');

const StudentZone_Internal_Schema = mongoose.Schema({
    title: {
        type: String,
        required: true,
        trim: true
    },
    link: {
        type: String,
        required: true,
        trim: true
    },
    file_path: {
        type: String,
        required: true
    },
    file_mimetype: {
        type: String,
        required: true
    }
}, {
    timestamps: true
});

const Student_Internal = mongoose.model('Student_Internal', StudentZone_Internal_Schema);

module.exports = Student_Internal;