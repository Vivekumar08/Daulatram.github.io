const mongoose = require('mongoose');

const GE_Options_Schema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true
    },
    link: {
      type: String,
      required: true,
      trim: true
    },
    file_path: {
      type: String,
      required: true
    },
    file_mimetype: {
      type: String,
      required: true
    }
  },
  {
    timestamps: true
  }
);

const GE_Options = mongoose.model('GE_Options', GE_Options_Schema);

module.exports = GE_Options;