const mongoose = require('mongoose');

const Anti_Ragging_Schema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true
    },
    link: {
      type: String,
      required: true,
      trim: true
    },
    file_path: {
      type: String,
      required: true
    },
    file_mimetype: {
      type: String,
      required: true
    }
  },
  {
    timestamps: true
  }
);

const Ragging = mongoose.model('Ragging', Anti_Ragging_Schema);

module.exports = Ragging;