const mongoose = require('mongoose');

const AdmissionBulletin_Schema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true
    },
    link: {
      type: String,
      required: true,
      trim: true
    },
    file_path: {
      type: String,
      required: true
    },
    file_mimetype: {
      type: String,
      required: true
    }
  },
  {
    timestamps: true
  }
);

const Bulletin = mongoose.model('Bulletin', AdmissionBulletin_Schema);

module.exports = Bulletin;