const mongoose = require('mongoose');

const Fee_Structure_Schema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true
    },
    link: {
      type: String,
      required: true,
      trim: true
    },
    file_path: {
      type: String,
      required: true
    },
    file_mimetype: {
      type: String,
      required: true
    }
  },
  {
    timestamps: true
  }
);

const Fee_Structure = mongoose.model('Fee_Structure', Fee_Structure_Schema);

module.exports = Fee_Structure;