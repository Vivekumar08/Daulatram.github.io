const mongoose = require('mongoose');

const Admission_comm_Schema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true
    },
    link: {
      type: String,
      required: true,
      trim: true
    },
    file_path: {
      type: String,
      required: true
    },
    file_mimetype: {
      type: String,
      required: true
    }
  },
  {
    timestamps: true
  }
);

const Admission_comm = mongoose.model('Admission_comm', Admission_comm_Schema);

module.exports = Admission_comm;