const mongoose = require('mongoose');

const Senior_list_Schema = mongoose.Schema({
    title: {
        type: String,
        required: true,
        trim: true
    },
    link: {
        type: String,
        required: true,
        trim: true
    },
    file_path: {
        type: String,
        required: true
    },
    file_mimetype: {
        type: String,
        required: true
    }
}, {
    timestamps: true
});

const Senior_list = mongoose.model('Senior_list', Senior_list_Schema);

module.exports = Senior_list;