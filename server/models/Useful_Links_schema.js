const mongoose = require('mongoose');

const Useful_Links_schema = mongoose.Schema({
    title: {
        type: String,
        required: true,
        trim: true
    },
    description: {
        type: String,
        required: true,
        trim: true
    },
    file_path: {
        type: String,
        required: true
    },
    file_mimetype: {
        type: String,
        required: true
    }
}, {
    timestamps: true
});

const Useful_Links = mongoose.model('Useful_Links', Useful_Links_schema);

module.exports = Useful_Links;