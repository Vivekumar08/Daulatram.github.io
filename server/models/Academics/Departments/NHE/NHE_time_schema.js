const mongoose = require('mongoose');

const time_Schema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true
    },
    link: {
      type: String,
      required: true,
      trim: true
    },
    file_path: {
      type: String,
      required: true
    },
    file_mimetype: {
      type: String,
      required: true
    }
  },
  {
    timestamps: true
  }
);

const NHE_time_Schema = mongoose.model('NHE_time_Schema', time_Schema);

module.exports = NHE_time_Schema;