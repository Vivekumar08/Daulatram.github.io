const mongoose = require('mongoose');

const Sanskrit_Awards_Schema = mongoose.Schema({
    title: {
        type: String,
        required: true,
        trim: true
    },
    link: {
        type: String,
        required: true,
        trim: true
    },
    file_path: {
        type: String,
        required: true
    },
    file_mimetype: {
        type: String,
        required: true
    }
}, {
    timestamps: true
});

const Sanskrit_Awards = mongoose.model('Sanskrit_Awards', Sanskrit_Awards_Schema);

module.exports = Sanskrit_Awards;