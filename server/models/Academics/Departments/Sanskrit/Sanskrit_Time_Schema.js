const mongoose = require('mongoose');

const Sanskrit_Time_Schema = mongoose.Schema({
    title: {
        type: String,
        required: true,
        trim: true
    },
    link: {
        type: String,
        required: true,
        trim: true
    },
    file_path: {
        type: String,
        required: true
    },
    file_mimetype: {
        type: String,
        required: true
    }
}, {
    timestamps: true
});

const Sanskrit_Time = mongoose.model('Sanskrit_Time', Sanskrit_Time_Schema);

module.exports = Sanskrit_Time;