const mongoose = require('mongoose');

const Psychology_Awards_Schema = mongoose.Schema({
    title: {
        type: String,
        required: true,
        trim: true
    },
    link: {
        type: String,
        required: true,
        trim: true
    },
    file_path: {
        type: String,
        required: true
    },
    file_mimetype: {
        type: String,
        required: true
    }
}, {
    timestamps: true
});

const Psychology_Awards = mongoose.model('Psychology_Awards', Psychology_Awards_Schema);

module.exports = Psychology_Awards;