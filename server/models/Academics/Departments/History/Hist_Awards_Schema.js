const mongoose = require('mongoose');

const Hist_Awards_Schema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true
    },
    link: {
      type: String,
      required: true,
      trim: true
    },
    file_path: {
      type: String,
      required: true
    },
    file_mimetype: {
      type: String,
      required: true
    }
  },
  {
    timestamps: true
  }
);

const Hist_Awards = mongoose.model('Hist_Awards', Hist_Awards_Schema);

module.exports = Hist_Awards;