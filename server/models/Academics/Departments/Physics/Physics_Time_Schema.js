const mongoose = require('mongoose');

const Physics_Time_Schema = mongoose.Schema({
    title: {
        type: String,
        required: true,
        trim: true
    },
    link: {
        type: String,
        required: true,
        trim: true
    },
    file_path: {
        type: String,
        required: true
    },
    file_mimetype: {
        type: String,
        required: true
    }
}, {
    timestamps: true
});

const Physics_Time = mongoose.model('Physics_Time', Physics_Time_Schema);

module.exports = Physics_Time;