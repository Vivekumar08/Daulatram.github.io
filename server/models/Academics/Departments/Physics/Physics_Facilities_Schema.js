const mongoose = require('mongoose');

const Physics_Facilities_Schema = mongoose.Schema({
    title: {
        type: String,
        required: true,
        trim: true
    },
    link: {
        type: String,
        required: true,
        trim: true
    },
    file_path: {
        type: String,
        required: true
    },
    file_mimetype: {
        type: String,
        required: true
    }
}, {
    timestamps: true
});

const Physics_Facilities = mongoose.model('Physics_Facilities', Physics_Facilities_Schema);

module.exports = Physics_Facilities;