const mongoose = require('mongoose');

const Physics_Events_Schema = mongoose.Schema({
    title: {
        type: String,
        required: true,
        trim: true
    },
    link: {
        type: String,
        required: true,
        trim: true
    },
    file_path: {
        type: String,
        required: true
    },
    file_mimetype: {
        type: String,
        required: true
    }
}, {
    timestamps: true
});

const Physics_Events = mongoose.model('Physics_Events', Physics_Events_Schema);

module.exports = Physics_Events;