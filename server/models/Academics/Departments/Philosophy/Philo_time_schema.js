const mongoose = require('mongoose');

const time_Schema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true
    },
    link: {
      type: String,
      required: true,
      trim: true
    },
    file_path: {
      type: String,
      required: true
    },
    file_mimetype: {
      type: String,
      required: true
    }
  },
  {
    timestamps: true
  }
);

const Philo_time_Schema = mongoose.model('Philo_time_Schema', time_Schema);

module.exports = Philo_time_Schema;