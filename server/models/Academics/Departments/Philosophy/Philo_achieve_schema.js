const mongoose = require('mongoose');

const Philo_achieve_Schema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true
    },
    link: {
      type: String,
      required: true,
      trim: true
    },
    file_path: {
      type: String,
      required: true
    },
    file_mimetype: {
      type: String,
      required: true
    }
  },
  {
    timestamps: true
  }
);

const Philo_achieve = mongoose.model('Philo_achieve', Philo_achieve_Schema);

module.exports = Philo_achieve;