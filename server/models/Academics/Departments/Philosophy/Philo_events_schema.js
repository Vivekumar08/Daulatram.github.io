const mongoose = require('mongoose');

const Philo_events_Schema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true
    },
    link: {
      type: String,
      required: true,
      trim: true
    },
    file_path: {
      type: String,
      required: true
    },
    file_mimetype: {
      type: String,
      required: true
    }
  },
  {
    timestamps: true
  }
);

const Philo_events = mongoose.model('Philo_events', Philo_events_Schema);

module.exports = Philo_events;