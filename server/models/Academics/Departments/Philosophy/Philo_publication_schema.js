const mongoose = require('mongoose');

const Philo_Publication_Schema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true
    },
    link: {
      type: String,
      required: true,
      trim: true
    },
    file_path: {
      type: String,
      required: true
    },
    file_mimetype: {
      type: String,
      required: true
    }
  },
  {
    timestamps: true
  }
);

const Philo_Publication = mongoose.model('Philo_Publication', Philo_Publication_Schema);

module.exports = Philo_Publication;