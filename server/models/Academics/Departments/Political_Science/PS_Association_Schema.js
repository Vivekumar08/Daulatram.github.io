const mongoose = require('mongoose');

const PS_Association_Schema = mongoose.Schema({
    title: {
        type: String,
        required: true,
        trim: true
    },
    link: {
        type: String,
        required: true,
        trim: true
    },
    file_path: {
        type: String,
        required: true
    },
    file_mimetype: {
        type: String,
        required: true
    }
}, {
    timestamps: true
});

const PS_Association = mongoose.model('PS_Association', PS_Association_Schema);

module.exports = PS_Association;