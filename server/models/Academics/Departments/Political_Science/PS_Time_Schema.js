const mongoose = require('mongoose');

const PS_Time_Schema = mongoose.Schema({
    title: {
        type: String,
        required: true,
        trim: true
    },
    link: {
        type: String,
        required: true,
        trim: true
    },
    file_path: {
        type: String,
        required: true
    },
    file_mimetype: {
        type: String,
        required: true
    }
}, {
    timestamps: true
});

const PS_Time = mongoose.model('PS_Time', PS_Time_Schema);

module.exports = PS_Time;