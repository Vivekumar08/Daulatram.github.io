const mongoose = require('mongoose');

const PE_Awards_Schema = mongoose.Schema({
    title: {
        type: String,
        required: true,
        trim: true
    },
    link: {
        type: String,
        required: true,
        trim: true
    },
    file_path: {
        type: String,
        required: true
    },
    file_mimetype: {
        type: String,
        required: true
    }
}, {
    timestamps: true
});

const PE_Awards = mongoose.model('PE_Awards', PE_Awards_Schema);

module.exports = PE_Awards;