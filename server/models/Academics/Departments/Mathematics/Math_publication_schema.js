const mongoose = require('mongoose');

const Math_Publication_Schema = mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      trim: true
    },
    link: {
      type: String,
      required: true,
      trim: true
    },
    file_path: {
      type: String,
      required: true
    },
    file_mimetype: {
      type: String,
      required: true
    }
  },
  {
    timestamps: true
  }
);

const Math_Publication = mongoose.model('Math_Publication', Math_Publication_Schema);

module.exports = Math_Publication;